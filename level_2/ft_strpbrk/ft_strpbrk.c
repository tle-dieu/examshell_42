char	*ft_strpbrk(const char *s, const char *s2)
{
	int i;

	while (*s)
	{
		i = 0;
		while (s2[i])	
		{
			if (s2[i] == *s)
				return ((char *)s);
			i++;
		}
		s++;
	}
	return (0);
}

#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char *new;
	int i;

	i = 0;
	while (src[i])
		i++;
	if (!(new = (char *)malloc(sizeof(char) * i + 1)))
		return (NULL);	
	i = 0;
	while (src[i])
	{
		new[i] = src[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
